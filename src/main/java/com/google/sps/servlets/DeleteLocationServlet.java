/** Servlet responsible for deleting locations. */
@WebServlet("/delete-location")
public class DeleteLocationServlet extends HttpServlet {

  @Override
  public void doPost(HttpServletRequest request, HttpServletResponse response) throws IOException {
    long id = Long.parseLong(request.getParameter("id"));

    Datastore datastore = DatastoreOptions.getDefaultInstance().getService();
    KeyFactory keyFactory = datastore.newKeyFactory().setKind("Location");
    Key locationEntityKey = keyFactory.newKey(id);
    datastore.delete(locationEntityKey);
  }
}